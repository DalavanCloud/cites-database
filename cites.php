function daisy($url)
{

	$url = 'https://daisy-pine.glitch.me/summary?q=' . urlencode($url);
	
	
	$json = get($url);
	
	
	if ($json != '')
	{
		$obj = json_decode($json);
		
		//print_r($obj);
		
		$guid = $url;
		
		if (isset($obj->DOI))
		{
			$guid = $obj->DOI;
		}
		
		if (isset($obj->reference))
		{
			$count = 1;
			
			foreach ($obj->reference as $cited)
			{
				$keys = array();
				$values = array();
				
				// Make sure we have a local id
				if (!isset($cited->key))
				{
					$cited->key = 'ref' . $count++;
				}				
				
				// can we parse unstructured?
				if (preg_match('/(?<authorstring>.*)\s+(?<year>[0-9]{4})[a-z]?\.\s+(?<title>.*)\.\s+(?<journal>.*)\s+(?<volume>\d+)(\s+\((?<issue>.*)\))?:\s+(?<spage>\d+)(â€“(?<epage>\d+))?\b/Uu', $cited->unstructured, $m))
				{
					$keymap = array(
						'authorstring' 	=> 'author',
						'year' 			=> 'year',
						'title' 		=> 'article-title',
						'journal' 		=> 'journal-title',
						'volume'	 	=> 'volume',
						'issue'			=> 'issue',
						'spage'			=> 'first-page',
						'epage'			=> 'last-page'
					);
				
					foreach ($m as $k => $v)
					{
						if (!is_numeric($k))
						{
							if ($v != '')
							{
								$cited->{$keymap[$k]} = $v;					
							}
						}
					}
				
				
				}
				
				
				foreach ($cited as $k => $v)
				{
					switch ($k)
					{
						case 'author':
							$keys[] = '`' . $k . '`';
							if (is_array($v))
							{
								$values[] = '"' . addcslashes(join(';', $v), '"') . '"';				
							}
							else
							{
								$values[] = '"' . addcslashes($v, '"') . '"';				
							}							
							break;							
					
						default:
							$keys[] = '`' . $k . '`';
							$values[] = '"' . addcslashes($v, '"') . '"';				
							break;
					}
				}
				
				$keys[] = 'guid';
				$values[] = '"' . $guid . '"';				
			
				$sql = 'REPLACE INTO cites(' . join(',', $keys) . ') values('
					. join(',', $values) . ');';
				echo $sql . "\n\n";
		
			
			}
		
		}
	
	}	
	
	
}

