								$keys[] = '`last-page`';
								$values[] = '"' . addcslashes($m['epage'], '"') . '"';				
							}
							break;	
			
						case 'title':
							switch ($csl->type)
							{
								case 'article-journal':
									$keys[] = '`article-title`';
									$values[] = '"' . addcslashes($v, '"') . '"';
									break;

								case 'book':
									$keys[] = '`volume-title`';
									$values[] = '"' . addcslashes($v, '"') . '"';
									break;

								case 'chapter':
									$keys[] = '`volume-title`';
									$values[] = '"' . addcslashes($v, '"') . '"';
									break;
			
			
								default:
									break;
							}
							break;
			
						case 'container-title':
							switch ($csl->type)
							{
								case 'article-journal':
									$keys[] = '`journal-title`';
									$values[] = '"' . addcslashes($v, '"') . '"';
									break;

								case 'chapter':
									$keys[] = '`series-title`';
									$values[] = '"' . addcslashes($v, '"') . '"';
									break;
			
			
								default:
									break;
							}
							break;
			
			
						case 'issued':
							if (isset($csl->issued->{'date-parts'}))
							{
								$keys[] = '`year`';
								$values[] = '"' . $csl->issued->{'date-parts'}[0][0] . '"';				
							}
							break;

	
	
						default:
							break;
					}	

				}

				$guid = '';

				if ($guid == '')
				{
					$guid = $obj->doi;
				}

				$keys[] = 'guid';
				$values[] = '"' . $guid . '"';				

				$sql = 'REPLACE INTO cites(' . join(',', $keys) . ') values('
					. join(',', $values) . ');';
				echo $sql . "\n\n";




			}
		}
	}
